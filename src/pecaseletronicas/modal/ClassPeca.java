
package pecaseletronicas.modal;

public class ClassPeca {

       public String peca;
       public String tipo;
       public String caixa;
       public String obs;
    /**
     * @return the peca
     */
    public String getPeca() {
        return peca;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @return the caixa
     */
    public String getCaixa() {
        return caixa;
    }

    /**
     * @return the obs
     */
    public String getObs() {
        return obs;
    }

    /**
     * @param peca the peca to set
     */
    public void setPeca(String peca) {
        this.peca = peca;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @param caixa the caixa to set
     */
    public void setCaixa(String caixa) {
        this.caixa = caixa;
    }

    /**
     * @param obs the obs to set
     */
    public void setObs(String obs) {
        this.obs = obs;
    }   
 
}
